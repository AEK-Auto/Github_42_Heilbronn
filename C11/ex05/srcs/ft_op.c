int	ft_op_add(int a, int b)
{
	return (a + b);
}

int	ft_op_sub(int a, int b)
{
	return (a - b);
}

int	ft_op_div(int a, int b)
{
	return (a / b);
}

int	ft_op_mul(int a, int b)
{
	return (a * b);
}

int	ft_op_mod(int a, int b)
{
	return (a % b);
}
